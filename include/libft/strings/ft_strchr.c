#include "../libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != '\0')
	{
		if (*s == (char)c)
			return ((char *)s);
	s++;
	}
	if ((char)c == *s)
		return ((char *)s);
	return (NULL);
}
